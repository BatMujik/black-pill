static uint8_t processing_buffer[SPI_BUFFER_SIZE];

// DEBUG: Add persistent buffer to see last received data
static uint8_t debug_last_rx[SPI_BUFFER_SIZE] = {0};
static volatile uint32_t debug_rx_count = 0;
static volatile uint8_t debug_has_data = 0;  

void HAL_SPI_TxRxCpltCallback(SPI_HandleTypeDef *hspi)
{
    if (hspi->Instance == SPI1)
    {
        // Toggle LED
        HAL_GPIO_TogglePin(GPIOC, GPIO_PIN_13);
        
        // Increment counter (watch this in live debug)
        debug_rx_count++;
        
        // Check if we received any non-zero data
        uint8_t has_data = 0;
        for (int i = 0; i < 20; i++) {  // Check first 20 bytes
            if (spi_rx_buffer[i] != 0) {
                has_data = 1;
                break;
            }
        }
        
        // Only update debug buffer if we got real data (not all zeros)
        if (has_data) {
            memcpy(debug_last_rx, (void*)spi_rx_buffer, SPI_BUFFER_SIZE);
            debug_has_data = 1;  // Flag that we have valid data
        }
        
        // Copy to processing buffer
        memcpy(processing_buffer, (void*)spi_rx_buffer, SPI_BUFFER_SIZE);
        
        // Prepare clean TX buffer
        memset((void*)spi_tx_buffer, 0, SPI_BUFFER_SIZE);
        
        // Restart SPI
        hspi->State = HAL_SPI_STATE_READY;
        HAL_SPI_TransmitReceive_IT(&hspi1, 
            (uint8_t*)spi_tx_buffer, 
            (uint8_t*)spi_rx_buffer, 
            SPI_BUFFER_SIZE);
        
        // Clear RX buffer after restart
        memset((void*)spi_rx_buffer, 0, SPI_BUFFER_SIZE);
        
        // Process data
        for (uint16_t i = 0; i < SPI_BUFFER_SIZE; ++i) {
            if (processing_buffer[i] != 0) {
                spi_rpc_on_rx_byte(processing_buffer[i]);
            }
        }
    }
}


void spi_rpc_send_response(const char *resp)
{
    if (resp && resp[0]) {
        size_t len = strlen(resp);
        if (len > SPI_BUFFER_SIZE) len = SPI_BUFFER_SIZE;
        memcpy((void*)spi_tx_buffer, resp, len);
    }
}

void HAL_SPI_ErrorCallback(SPI_HandleTypeDef *hspi)
{
    if (hspi->Instance == SPI1)
    {
        memset((void*)spi_tx_buffer, 0, SPI_BUFFER_SIZE);
        memset((void*)spi_rx_buffer, 0, SPI_BUFFER_SIZE);
        hspi->State = HAL_SPI_STATE_READY;
        HAL_SPI_TransmitReceive_IT(&hspi1, (uint8_t*)spi_tx_buffer, (uint8_t*)spi_rx_buffer, SPI_BUFFER_SIZE);
    }
}
